#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	i = 0;
	tmp = (unsigned char*)s;
	while (i < n)
	{
		if ((unsigned char)c == tmp[i])
			return (tmp + i);
		i++;
	}
	return (NULL);
}
